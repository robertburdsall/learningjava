public class Wrapperclasses {

    public static void main(String[] args){

     // Wrapper classes provide ways to use primitive data types as reference data types

        // Primitive - Boolean char int double (faster)

        // Wrapper - Boolean Character Integer Double

        // autoboxing - automatic conversion that java complier makes between primitive data types to wrappers

        // unboxing - wrappers to primitive - opposite of autoboxing


        Boolean a = true;
        Character b = '&';
        Integer c = 123;
        Double d = 3.14;
        String e = "Bro";

        if(a==true){
            System.out.println("This is true");
        }

    }
}
